"""
Rebuild pendo-docs skill reference files from live documentation sources.

Run periodically (e.g. weekly via cron or CI) to keep the agent's doc index current.
Each source writes its own Markdown file into the skill's references/ folder.

Usage (from professor-pendo repo root, after `uv sync`):

    uv run .agents/SKILLS/pendo/pendo-docs/scripts/refresh_docs.py
    uv run .agents/SKILLS/pendo/pendo-docs/scripts/refresh_docs.py --source help-center

If you need the local `professor_pendo` package (unpublished edits), add `--with-editable .`
before the script path.
"""

